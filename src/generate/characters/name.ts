export function getName(race: TRace, sex: boolean = true, random = Math.random) {
  const variants = names[race]
  if (!variants) return null
  const variant = variants[Math.floor(random() * variants.length)]
  const [n1, n2] = variant.split(";")
  return sex ? n1 : n2 || n1
}
